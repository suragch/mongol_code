  static bool isVowel(int codeUnit) {
    return (codeUnit >= A && codeUnit <= EE);
  }

  static bool isMasculineVowel(int codeUnit) {
    return (codeUnit == A || codeUnit == O || codeUnit == U);
  }

  static bool isFeminineVowel(int codeUnit) {
    return (codeUnit == E ||
        codeUnit == EE ||
        codeUnit == OE ||
        codeUnit == UE);
  }

  static bool isConsonant(int codeUnit) {
    return (codeUnit >= NA && codeUnit <= CHI);
  }

  static bool isFVS(int codeUnit) {
    return (codeUnit >= FVS1 && codeUnit <= FVS3);
  }

  /// Mongolian Combining Consonant
  static bool isMcc(int? character) {
    return character == BA ||
        character == PA ||
        character == FA ||
        character == KA ||
        character == KHA;
  }

  /// Mongolian Conditional Combining Consonant
  static bool isMccc(int? character) {
    return character == QA || character == GA;
  }
}
